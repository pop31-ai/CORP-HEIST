def gen_hero_pdf(key, name, tier, color_hex, desc_text):
    path = os.path.join(HERO_PDFS, f"hero_{key}.pdf")
    c = canvas.Canvas(path, pagesize=A4)
    w, h = A4
    color = HexColor(color_hex)

    c.setFillColor(HexColor("#0A0A12"))
    c.rect(0, 0, w, h, fill=1, stroke=0)

    c.setFillColor(color)
    c.setFont("Helvetica-Bold", 28)
    c.drawString(50, h - 60, f"{name}")

    c.setFillColor(HexColor("#FFFFFF"))
    c.setFont("Helvetica", 14)
    c.drawString(50, h - 85, f"Tier: {tier}")

    c.setStrokeColor(color)
    c.setLineWidth(2)
    c.line(50, h - 95, w - 50, h - 95)

    y = h - 120
    c.setFillColor(HexColor("#CCCCCC"))
    c.setFont("Helvetica", 10)
    for line in desc_text.strip().split("\n"):
        line = line.strip()
        if not line:
            y -= 8
            continue
        c.drawString(50, y, line)
        y -= 16
        if y < 50:
            c.showPage()
            c.setFillColor(HexColor("#0A0A12"))
            c.rect(0, 0, w, h, fill=1, stroke=0)
            y = h - 50

    c.setFillColor(color)
    c.setFont("Helvetica-Bold", 10)
    c.drawString(50, 30, f"CORP HEIST — Hero Design Document — {name} ({tier})")

    c.save()
    return path


print("Генерация PDF-набросков героев...")
pdfs = []
for key, name, tier, color, desc in HEROES_PDF:
    p = gen_hero_pdf(key, name, tier, color, desc)
    pdfs.append(p)
    print(f"  {p}")
print(f"Создано {len(pdfs)} PDF")


